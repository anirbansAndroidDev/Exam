package ica.ProfileInfo;

public class DateInfo {

	int Year;
	int Month;
	int DayOfMonth;
	
	public int getYear() {
		return Year;
	}
	public void setYear(int year) {
		Year = year;
	}
	public int getMonth() {
		return Month;
	}
	public void setMonth(int month) {
		Month = month;
	}
	public int getDayOfMonth() {
		return DayOfMonth;
	}
	public void setDayOfMonth(int dayOfMonth) {
		DayOfMonth = dayOfMonth;
	}
}
